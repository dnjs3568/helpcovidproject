package model1;

public class HealthCenterTO {
	private String sido;
	private String sigungu;
	private String name;
	private String address;
	private String hours;
	private String sat_hours;
	private String holiday_hours;
	private String tel;
	
	public String getSido() {
		return sido;
	}
	public void setSido(String sido) {
		this.sido = sido;
	}
	public String getSigungu() {
		return sigungu;
	}
	public void setSigungu(String sigungu) {
		this.sigungu = sigungu;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getHours() {
		return hours;
	}
	public void setHours(String hours) {
		this.hours = hours;
	}
	public String getSat_hours() {
		return sat_hours;
	}
	public void setSat_hours(String sat_hours) {
		this.sat_hours = sat_hours;
	}
	public String getHoliday_hours() {
		return holiday_hours;
	}
	public void setHoliday_hours(String holiday_hours) {
		this.holiday_hours = holiday_hours;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	
}
